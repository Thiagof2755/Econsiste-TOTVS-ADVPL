SELECT
    SC5.C5_CLIENTE,
    SA1.A1_NOME,
    SA1.A1_CGC,
    SA1.A1_ENDREC,
    SA1.A1_EMAIL,
    SC6.C6_ENTREG, -- Data prevista da entrega do pedido.
    SC6.C6_NUM,
    SC6.C6_PRODUTO,
    SC6.C6_PRCVEN,
    SC6.C6_ITEM,
    SC6.C6_QTDLIB,
    SC6.C6_QTDENT,
    SC9.C9_QTDLIB,
    SC6.C6_BLQ,
    ((SC9.C9_QTDLIB - SC6.C6_QTDLIB) / 12) AS QTD_CAIXAS, -- Quantidade de caixas considerando 12 unidades por caixa
    ((SC9.C9_QTDLIB - SC6.C6_QTDLIB) / 12) * SC6.C6_PRCVEN AS VALOR_TOTAL_CAIXAS -- Valor total das caixas
FROM 
    SC5990 AS SC5
INNER JOIN 
    SC6990 AS SC6 
    ON SC5.C5_NUM = SC6.C6_NUM
    AND SC5.C5_CLIENTE = SC6.C6_CLI
    AND SC5.C5_FILIAL = SC6.C6_FILIAL
    AND SC5.D_E_L_E_T_ = SC6.D_E_L_E_T_
INNER JOIN
    SC9990 AS SC9 
    ON SC9.C9_PRODUTO = SC6.C6_PRODUTO
    AND SC9.C9_FILIAL = SC6.C6_FILIAL
    AND SC9.D_E_L_E_T_ = SC6.D_E_L_E_T_
INNER JOIN 
    SA1990 AS SA1 
    ON SA1.A1_COD = SC5.C5_CLIENTE
    AND SA1.D_E_L_E_T_ = SC5.D_E_L_E_T_
WHERE 
	C6_BLQ <> 'S'
	AND (
    C9_QTDLIB >= C6_QTDLIB
	OR C6_QTDLIB < C6_QTDENT)
	